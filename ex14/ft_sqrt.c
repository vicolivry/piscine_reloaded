int	ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb < 0 || nb > 2147483647)
		return (0);
	if (nb == 0 || nb == 1)
		return (nb);
	while (i <= nb / i)
	{
		if (i * i == nb)
		{
			if (nb % i == 0)
				return (i);
		}
		i++;
	}
	return (0);
}
